// 获取特定层级的地区数据
export const getRegionsByLevel = (level: 'world' | 'country' | 'province' | 'city', parentId?: string) => {
  return Object.values(mockRegionsData).filter(region => {
    if (level === 'world') return region.type === 'world'
    if (level === 'country') return region.type === 'country'
    if (parentId) return region.type === level && region.parentId === parentId
    return region.type === level
  })
}

// 获取顶级国家数据（用于世界地图）
export const getTopCountries = () => {
  return getRegionsByLevel('country').sort((a, b) => b.tangpingIndex - a.tangpingIndex)
}

// 获取中国省份数据
export const getChinaProvinces = () => {
  return getRegionsByLevel('province', 'CN').sort((a, b) => b.tangpingIndex - a.tangpingIndex)
} 